"""PERCABANGAN DENGAN CONTOH KALKULATOR SEDERHANA"""

angka1 = float(input("Masukkan angka pertama = "))
operator = input("Masukkan operator = ")
angka2 = float(input("Masukkan angka kedua = "))

"""

"""
if operator == "+":
    hasil = angka1 + angka2
    print(f"Hasil = {hasil}")
elif operator == "*":
    hasil = angka1 * angka2
    print(f"Hasil = {hasil}")
elif operator == "-":
    hasil = angka1 - angka2
    print(f"Hasil = {hasil}")
elif operator == "/" or ":":
    hasil = angka1 / angka2
    print(f"Hasil = {hasil}")
else:
    print("Data tidak valid!, Coba lagi!")
